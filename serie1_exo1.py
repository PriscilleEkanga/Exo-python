#Exercice1: Variables types et calculs

# On définit les dimensions du rectangle
largeur = 4        # un nombre entier
hauteur = 4.0      # un nombre flottant
largeur = float(input("Largeur ? "))
hauteur = float(input("hauteur ? "))

# Calculs
surface = largeur * hauteur
perimetre = 2 * (largeur + hauteur)

# Affichage
print("Surface :", surface)
print("Périmètre :", perimetre)




